      ,[STUDENT_PASSWORD]
  FROM [STUDENT]

  SELECT TOP (1000) [MODULE_ID]
      ,[MODULE_CODE]
      ,[MODULE_NAME]
      ,[MODULE_CREDITS]
      ,[CLASS_HOURS_PER_WEEK]
      ,[STUDENT_USERNAME]
  FROM [MODULE]

