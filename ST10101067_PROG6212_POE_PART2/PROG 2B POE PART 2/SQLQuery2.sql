      ,[SEMESTER_WEEKS]
      ,[SEMESTER_START_DATE]
      ,[SEMESTER_END_DATE]
      ,[STUDENT_USERNAME]
 FROM [SEMESTER]

  SELECT TOP (1000) [STUDY_ID]
      ,[SELF_STUDY_HOURS_PER_WEEK]
      ,[SELECTED_DATE]
      ,[START_TIME]
      ,[END_TIME]
      ,[HOURS_STUDIED]
      ,[STUDY_HOURS_REMAINDING]
      ,[CURRENT_WEEK]
      ,[MODULE_CODE]
      ,[STUDENT_USERNAME]
  FROM [STUDY]
